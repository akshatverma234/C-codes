#include <stdio.h>

int main(){
    int a,b;
    printf("Enter the first number\n");
    scanf("%d",&a);
    printf("Enter the second number\n");
    scanf("%d",&b);
    printf("The Sum of Two Numbers are: %d\n",a+b);
    printf("The Substraction of Two Numbers are: %d\n",a-b);
    printf("The Multiple of Two Numbers are: %d\n",a*b);
    printf("The Division of Two Numbers are: %d\n",a/b);
    printf("The Modulas of Two Numbers are: %d\n",a%b);
    return 0;
}